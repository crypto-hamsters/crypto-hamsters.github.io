const hamstersInfo = (total) => {
	let hamsters = document.querySelector(".hamsters");
	console.log("hamsters", hamsters);
	for (let i = 0; i < total; i++) {
		const j = i;
		let hamster = document.createElement("div");
		hamster.className = "hamster";
		hamsters.appendChild(hamster);
		cryptoHamstersContract.nameByIndex(i, function (error, result) {
		    if (!error) {
		        console.log(result.toString());
		    	let price = document.createElement("p");
		    	price.className = "price";
		   		price.appendChild(document.createTextNode("100ETH"));
		    	let name = document.createElement("h2");
		    	name.className = "name";
		   		name.appendChild(document.createTextNode(result));
		    	hamster.appendChild(price);
		    	hamster.appendChild(name);
				cryptoHamstersContract.genesByIndex(j, function (error, result) {
					if (!error) {
				    	let image = document.createElement("img");
				    	let choosePicture = result.modulo(2) == 0 ? "cryptohamster-white-orange" : "cryptohamster-white";
				    	console.log(result.toString());
				    	image.setAttribute("src", `images/${choosePicture}.png`);
		    			hamster.appendChild(image);
					}
					else
						console.error(error);
				})	
				let button = document.createElement("button");
		   		button.appendChild(document.createTextNode("Buy"));	
		    	hamster.appendChild(button);		
		    }
		    else
		        console.error(error);
		});
	}
};



